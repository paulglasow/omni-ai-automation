#!/usr/bin/env node
/**
 * OmniAI v4 — Environment File Generator
 *
 * Generates .env.template and .env files with all required variables.
 * Handles secure credential storage — tokens are never logged.
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, '..');

/**
 * Generate or update the .env file with values from config.
 *
 * @param {object} config - Key/value pairs to write to .env
 * @param {object} [options]
 * @param {boolean} [options.overwrite=false] - Overwrite existing values
 * @returns {{ envPath: string, templatePath: string, success: boolean }}
 */
export function generateEnvFile(config = {}, { overwrite = false } = {}) {
  const envPath = path.join(ROOT, '.env');
  const templatePath = path.join(ROOT, '.env.template');

  // ── Read existing .env if present ─────────────────────────────────────────
  const existing = parseEnvFile(envPath);

  // ── Merge: existing values take priority unless overwrite=true ─────────────
  const merged = overwrite
    ? { ...existing, ...config }
    : { ...config, ...existing };

  // ── Build the new .env content ─────────────────────────────────────────────
  const sections = [
    {
      comment: '# Automation tokens',
      keys: ['GITHUB_TOKEN', 'VERCEL_TOKEN', 'SUPABASE_TOKEN'],
    },
    {
      comment: '# AI API Keys',
      keys: ['OPENAI_API_KEY', 'GEMINI_API_KEY', 'PERPLEXITY_API_KEY', 'GITHUB_COPILOT_TOKEN'],
    },
    {
      comment: '# Supabase project (auto-filled by setup)',
      keys: ['SUPABASE_URL', 'SUPABASE_ANON_KEY', 'SUPABASE_SERVICE_ROLE_KEY'],
    },
    {
      comment: '# Vercel deployment (auto-filled by setup)',
      keys: ['VERCEL_ORG_ID', 'VERCEL_PROJECT_ID'],
    },
    {
      comment: '# GitHub repository (auto-filled by setup)',
      keys: ['GITHUB_USERNAME', 'GITHUB_REPO_NAME'],
    },
  ];

  const lines = [
    '# OmniAI v4 — Environment Variables',
    '# Generated by scripts/env-generator.js',
    '# DO NOT commit this file to git',
    '',
  ];

  // Collect all known keys
  const knownKeys = new Set(sections.flatMap((s) => s.keys));

  for (const { comment, keys } of sections) {
    lines.push(comment);
    for (const key of keys) {
      lines.push(`${key}=${merged[key] ?? ''}`);
    }
    lines.push('');
  }

  // Add any extra keys not in the sections
  const extraKeys = Object.keys(merged).filter((k) => !knownKeys.has(k));
  if (extraKeys.length > 0) {
    lines.push('# Additional variables');
    for (const key of extraKeys) {
      lines.push(`${key}=${merged[key] ?? ''}`);
    }
    lines.push('');
  }

  const envContent = lines.join('\n');

  // ── Write .env file ────────────────────────────────────────────────────────
  fs.writeFileSync(envPath, envContent, { encoding: 'utf-8', mode: 0o600 });
  console.log(`  ✅ .env file written: ${envPath}`);

  // ── Copy template if it doesn't exist yet ─────────────────────────────────
  if (!fs.existsSync(templatePath)) {
    const templateSrc = path.join(ROOT, '.env.template');
    if (fs.existsSync(templateSrc)) {
      fs.copyFileSync(templateSrc, templatePath);
    }
  }

  return { envPath, templatePath, success: true };
}

/**
 * Parse an existing .env file into a key/value object.
 * Lines starting with '#' and empty lines are ignored.
 *
 * @param {string} filePath - Path to .env file
 * @returns {object} Parsed key/value pairs
 */
export function parseEnvFile(filePath) {
  if (!fs.existsSync(filePath)) return {};

  const result = {};
  const lines = fs.readFileSync(filePath, 'utf-8').split('\n');

  for (const line of lines) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;

    const eqIdx = trimmed.indexOf('=');
    if (eqIdx === -1) continue;

    const key = trimmed.slice(0, eqIdx).trim();
    const value = trimmed.slice(eqIdx + 1).trim().replace(/^["']|["']$/g, '');

    if (key) result[key] = value;
  }

  return result;
}

// ── CLI entry point ──────────────────────────────────────────────────────────
if (process.argv[1] === fileURLToPath(import.meta.url)) {
  generateEnvFile({}, { overwrite: false });
  console.log('\n✅ .env file generated. Open it and fill in your API keys.');
  console.log('   Template: .env.template');
  console.log('   See also: TOKENS_NEEDED.md for instructions on each key.\n');
}
